def kbp_to_list(kbp):
  kbp = kbp.split('>')
  l = len(kbp[1])
  nlist = []
  for i in kbp:
    if len(i) == 0:
      pass
    elif len(i) != l:
      n = i[11:]
      nlist.append(n)
    else:
      n = i[11:-1]
      nlist.append(n)
  return(nlist)

def l_to_s(l):
  s = ""
  for i in l:
    i = str(i)
    s = s + i + ' '
  return(s)

def findl(l,ls):
  indexer = 0 
  consensus_for_l = []
  while indexer < len(ls[0]):
    counter = 0 
    for i in ls:
      if i[indexer] == l:
        counter = counter + 1
      else:
        pass
    consensus_for_l.append(counter)
    indexer = indexer + 1
  return(consensus_for_l)


def consensus_maker(kbp):
  s = kbp_to_list(kbp)
  return ('A: ' + l_to_s(findl('A',s)) + '\n' + 'C: ' + l_to_s(findl('C',s)) + '\n' + 'G: ' + l_to_s(findl('G',s)) + '\n' + 'T: ' + l_to_s(findl('T',s)))

def profiler(kbp):
  s = kbp_to_list(kbp)
  Al = findl('A',s)
  Cl = findl('C',s)
  Gl = findl('G',s)
  Tl = findl('T',s)
  i = 0
  profile = ''
  while i < len(Al):
    if Al[i] > Cl[i] and Al[i] > Tl[i] and Al[i] > Gl[i]:
      profile += 'A'
    elif Cl[i] > Al[i] and Cl[i] > Tl[i] and Cl[i] > Gl[i]:
      profile += 'C'
    elif Tl[i] > Al[i] and Tl[i] > Cl[i] and Tl[i] > Gl[i]:
      profile += 'T'
    else:
      profile += 'G'
    i = i + 1
  return(profile)

